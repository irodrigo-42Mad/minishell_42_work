#include "libft.h"

int	ft_lstsize(t_list *lst)
{
	int	i;

	i = 0;
	if (lst)
	{
		while (lst != '\0')
		{
			lst = lst->next;
			i++;
		}
		return (i);
	}
	return (0);
}
