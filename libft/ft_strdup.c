#include "libft.h"

static char	*ft_strcpy(char *dst, const char *src)
{
	int	i;

	i = 0;
	while (src[i] != '\0')
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (dst);
}

char	*ft_strdup(const char *s1)
{
	char	*c;

	c = (char *)malloc(ft_strlen(s1) + 1);
	if (!c)
		return (0);
	ft_strcpy(c, (char *)s1);
	return (c);
}
